#Int or integer, it a whole number, positive or negative, without decimal, of unlimited length

a = 5
b = -323823
c = 2392643131643164656

print(c)
print(type(a))
print(type(b))
print(type(c))

#float or floating point numbers, positive or negative, contain one or more decimal

x = 3.2
y = -32.25
z = 1.6468

print(type(x))
print(type(y))
print(type(z))

#float can also be a scientific number with an "e" to indicate the power of 10

X = 25e6
Y = 68E53
Z = -32.7e32

print(type(X))
print(type(Y))
print(type(Z))

#Complex number are written with a "j" as the imaginary part

A = 3+4j
B = 5j
C = -5j

print(B)
print(C)
print(type(A))
print(type(B))
print(type(C))